string=input("请输入一段字符串:")
print(len(string))										#统计字符个数
print(max(string))										#最大的字符
print(min(string))										#最小的字符
print(string.upper())									#所有字母大写
print(string.lower())									#所有字母小写
print(string.swapcase())								#改变字母的大小写
print(string.title())										#每个单词首字母大写，其他小写
print(string.find("Python"))								#查找单词Python的位置